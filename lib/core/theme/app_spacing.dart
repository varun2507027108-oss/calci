///
/// Consistent rhythm throughout the app. Every margin, padding,
/// and gap uses these values exclusively.
abstract final class AppSpacing {
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 12.0;
  static const double lg = 16.0;
  static const double xl = 24.0;
  static const double xxl = 32.0;
  static const double xxxl = 48.0;
  static const double huge = 64.0;

  /// Keypad button gap
  static const double keypadGap = 8.0;

  /// Display panel padding
  static const double displayPadding = 24.0;

  /// Bottom nav height
  static const double bottomNavHeight = 72.0;

  /// Safe area padding for notch devices
  static const double safeAreaBottom = 34.0;
}
